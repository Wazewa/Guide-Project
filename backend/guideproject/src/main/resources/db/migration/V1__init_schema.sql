CREATE TABLE IF NOT EXISTS Person (
      person_id INTEGER  PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
      email VARCHAR(64) NOT NULL UNIQUE,
      name VARCHAR(64) NOT NULL,
      surname VARCHAR(64) NOT NULL,
      hash_password VARCHAR(128) NOT NULL
);
CREATE TABLE IF NOT EXISTS Landmark_Category (
    landmark_category_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(64) NOT NULL UNIQUE
);
CREATE TABLE IF NOT EXISTS Landmark (
    landmark_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(64) NOT NULL,
    coordinates VARCHAR(128) NOT NULL,
    description TEXT NOT NULL,
    address VARCHAR(64) NOT NULL,
    landmark_category_id INTEGER REFERENCES Landmark_Category(landmark_category_id) ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS Rating (
    rating_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    grade DECIMAL(5,2) CHECK (grade >= 1 AND grade <=5),
    person_id INTEGER REFERENCES Person(person_id) ON DELETE CASCADE,
    landmark_id INTEGER REFERENCES Landmark(landmark_id) ON DELETE CASCADE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (person_id, landmark_id)
);
CREATE TABLE IF NOT EXISTS Review (
    review_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    review_text TEXT NOT NULL,
    person_id INTEGER REFERENCES Person(person_id) ON DELETE CASCADE,
    landmark_id INTEGER REFERENCES Landmark(landmark_id) ON DELETE CASCADE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
)